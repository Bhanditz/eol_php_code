    <div >
     Like the herring, there are various races of cod, which differ in their growth rates, distribution and times of spawning. Most cod spawn between the months of January and April and a female, if she is large enough, can release up to five million eggs. Depending on the temperature, the eggs hatch in two to four weeks and the young cod drift in the open ocean, feeding on small crustaceans. Atlantic cod will eat a wide variety of prey, ranging from other fish (up to the size of herring) to worms; they also take swimming crabs, shrimps and prawns. </p><p>The different races of this fish vary in the ages and weights attained before they become sexually mature. The migratory cod found off the coasts of Newfoundland, Iceland and Norway mature at around eight to 12 years old when they may weigh up to eight kilogrammes. Coastal cod mature more rapidly and may be able to reproduce at the age of three years.
